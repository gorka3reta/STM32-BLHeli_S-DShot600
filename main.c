void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  if (htim == &htim11) {


		 // wait power up
		 if (ctr < ESC_POWER_UP) {
			  ++ctr;
			  return;
		  }

		 dshot600(motor,value);
		 HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, motor, DSHOT_FRAME_SIZE);

		  if (ctr > ESC_POWER_UP+TROTTLE_MAX*6){

			 armed = 1;	//the arming sequence is ended, check this var in your own code to change value's value
			 return;
		  }

		  ++ctr;

		 if (trottle_down) {
			 --value;
			 if (value<TROTTLE_MIN)
				 value=TROTTLE_MIN;
		 }
		 else {
			 ++value;
			 if (value > TROTTLE_MAX) {
				 value = TROTTLE_MAX;
				 trottle_down=1;
			 }
		 }
  }
}
